    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        int result = fileChooser.showOpenDialog(null);
        File selectedFile = fileChooser.getSelectedFile();
        FileReader file = null; 
        try {
            file = new FileReader(selectedFile); //path to file
        } catch (FileNotFoundException ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        BufferedReader br = new BufferedReader(file);
        String line = null;
        try {
            while((line = br.readLine()) != null){
               this.listNames.add(line);
            }
        } catch (IOException ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        jLabel3.setText("A quantidade de casais participantes Ã© de: " + this.listNames.size());
        jLabel3.setVisible(true);
        jButton1.setEnabled(true);
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      
        if(this.sort == true){
            jLabel4.setVisible(false);
            jLabel1.setVisible(true);
            this.sort = false;
            marioTheme.play();
            immunityTheme.close();
        }
        else{
           jLabel1.setVisible(false);
           this.sort = true;
           int numberFriend = (int) (Math.random() * (this.listNames.size() - 1));
           jLabel4.setText(this.listNames.get(numberFriend));
           jLabel4.setVisible(true);
           marioTheme.close();
           immunityTheme.play();
           this.listNames.remove(numberFriend);
        }
               
    }//GEN-LAST:event_jButton1ActionPerformed

